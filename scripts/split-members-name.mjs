/**
 * 既存 members の name / furigana を first_name, last_name / first_furigana, last_furigana に分割するバッチ。
 * 使い方: node scripts/split-members-name.mjs [_Prep/members_rows.csv]
 * 出力: SQL UPDATE 文を stdout に出す。045 マイグレーション適用後に Supabase SQL Editor で実行する。
 */

import fs from "fs";
import path from "path";

const CSV_PATH =
  process.argv[2] ?? path.join(process.cwd(), "_Prep", "members_rows.csv");

const ONE_CHAR_SURNAMES = new Set(
  "林原森谷池井伊吉西安石藤東北南中上下山口本木松竹梅目米貝車金長高小大新古多".split("")
);

function hasKanji(s) {
  return /[\u4e00-\u9faf\u3400-\u4dbf]/.test(s);
}

function inferLastNameLength(name) {
  const t = name.trim();
  if (!t) return 0;
  if (t.length >= 4) return 2;
  if (t.length === 3) return ONE_CHAR_SURNAMES.has(t[0]) ? 1 : 2;
  if (t.length === 2) return 1;
  return 0;
}

function inferLastFuriganaLength(lastNameLength) {
  if (lastNameLength <= 0) return 0;
  if (lastNameLength === 1) return 3;
  return lastNameLength + 1;
}

function splitNameAndFurigana(name, furigana) {
  const n = (name ?? "").trim();
  const f = (furigana ?? "").trim();

  if (!n) {
    return {
      last_name: "",
      first_name: "",
      last_furigana: "",
      first_furigana: f,
    };
  }

  if (!hasKanji(n)) {
    return {
      last_name: "",
      first_name: n,
      last_furigana: "",
      first_furigana: f,
    };
  }

  const lastLen = inferLastNameLength(n);
  const last_name = lastLen > 0 ? n.slice(0, lastLen) : "";
  const first_name = lastLen > 0 ? n.slice(lastLen) : n;

  if (!f) {
    return {
      last_name,
      first_name,
      last_furigana: "",
      first_furigana: "",
    };
  }

  const lastFuriganaLen = Math.min(
    inferLastFuriganaLength(lastLen),
    Math.max(0, f.length - 1)
  );
  const last_furigana = lastFuriganaLen > 0 ? f.slice(0, lastFuriganaLen) : "";
  const first_furigana = lastFuriganaLen > 0 ? f.slice(lastFuriganaLen) : f;

  return {
    last_name,
    first_name,
    last_furigana,
    first_furigana,
  };
}

function parseCsvRow(line) {
  const result = [];
  let current = "";
  let inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (c === '"') {
      inQuotes = !inQuotes;
    } else if ((c === "," && !inQuotes) || (c === "\n" && !inQuotes)) {
      result.push(current);
      current = "";
      if (c === "\n") break;
    } else {
      current += c;
    }
  }
  result.push(current);
  return result;
}

function escapeSql(s) {
  return s.replace(/'/g, "''");
}

const content = fs.readFileSync(CSV_PATH, "utf-8");
const lines = content.split(/\r?\n/).filter((l) => l.trim());
if (lines.length < 2) {
  console.error("CSV has no header or rows.");
  process.exit(1);
}

const header = parseCsvRow(lines[0]);
const nameIdx = header.indexOf("name");
const furiganaIdx = header.indexOf("furigana");
const idIdx = header.indexOf("id");
if (nameIdx < 0 || idIdx < 0) {
  console.error("CSV must have 'id' and 'name' columns.");
  process.exit(1);
}

console.log("-- Generated by scripts/split-members-name.mjs");
console.log("-- Run after applying migration 045_members_first_last_name.sql");
console.log("BEGIN;");

for (let i = 1; i < lines.length; i++) {
  const row = parseCsvRow(lines[i]);
  const id = row[idIdx]?.trim();
  const name = row[nameIdx]?.trim() ?? "";
  const furigana =
    furiganaIdx >= 0 ? (row[furiganaIdx]?.trim() ?? null) : null;
  if (!id) continue;

  const { last_name, first_name, last_furigana, first_furigana } =
    splitNameAndFurigana(name, furigana);

  const sql = [
    `last_name = ${last_name ? `'${escapeSql(last_name)}'` : "NULL"}`,
    `first_name = ${first_name ? `'${escapeSql(first_name)}'` : "NULL"}`,
    `last_furigana = ${last_furigana ? `'${escapeSql(last_furigana)}'` : "NULL"}`,
    `first_furigana = ${first_furigana ? `'${escapeSql(first_furigana)}'` : "NULL"}`,
  ].join(", ");
  console.log(`UPDATE members SET ${sql} WHERE id = '${escapeSql(id)}';`);
}

console.log("COMMIT;");
